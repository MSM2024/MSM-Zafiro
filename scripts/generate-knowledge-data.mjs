import fs from "fs"
import path from "path"
import { fileURLToPath } from "url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const kpDir = path.join(__dirname, "..", "knowledge-pack", "zafiro-eliana-kp-v1-es-us-2026-07-08")
const outFile = path.join(__dirname, "..", "src", "lib", "knowledge-data.ts")

function parseFrontMatter(raw) {
  const meta = {}
  let body = raw.trim()
  if (body.startsWith("---")) {
    const end = body.indexOf("---", 3)
    if (end !== -1) {
      const front = body.slice(3, end).trim()
      body = body.slice(end + 3).trim()
      for (const line of front.split("\n")) {
        const colonIdx = line.indexOf(":")
        if (colonIdx === -1) continue
        const key = line.slice(0, colonIdx).trim()
        let val = line.slice(colonIdx + 1).trim()
        if (val.startsWith("[") && val.endsWith("]")) {
          val = val.slice(1, -1).split(",").map((s) => s.trim().replace(/^["']|["']$/g, ""))
        } else if (val === "true" || val === "false") {
          val = val === "true"
        } else if (!isNaN(Number(val))) {
          val = Number(val)
        } else {
          val = val.replace(/^["']|["']$/g, "")
        }
        meta[key] = val
      }
    }
  }
  return { meta, content: body }
}

const docs = []
const entries = fs.readdirSync(kpDir, { withFileTypes: true }).filter(e => e.isDirectory())
for (const entry of entries) {
  const dirPath = path.join(kpDir, entry.name)
  const files = fs.readdirSync(dirPath).filter(f => f.endsWith(".md"))
  for (const file of files) {
    const raw = fs.readFileSync(path.join(dirPath, file), "utf-8")
    const { meta, content } = parseFrontMatter(raw)
    docs.push({
      id: meta.id || file.replace(".md", ""),
      title: meta.title || file.replace(".md", ""),
      path: `${entry.name}/${file}`,
      dataset: meta.category || entry.name,
      doc_form: meta.doc_form || "text_model",
      content,
      tags: Array.isArray(meta.tags) ? meta.tags : [],
      language: meta.language || "es",
    })
  }
}

const ts = `// Auto-generated by scripts/generate-knowledge-data.mjs
// Do not edit manually. Run: npm run build:knowledge
export interface KnowledgeDoc {
  id: string
  title: string
  path: string
  dataset: string
  doc_form: string
  content: string
  tags: string[]
  language: string
}

export const KNOWLEDGE_DOCS: KnowledgeDoc[] = ${JSON.stringify(docs, null, 2)}
`

fs.writeFileSync(outFile, ts, "utf-8")
console.log(`Generated ${docs.length} knowledge docs → ${outFile}`)
